package class2;

public class OperatorExample {
    public static void main(String[] args) {
//      1.  Arithmetic operators

        //assigned
        int a = 10;
        int b = 20;

        int sum = a + b;
        System.out.println(sum);


        //increment
        int c = 10;
        int d = 1;

        d += c;
        System.out.println(d);


        int val1 = 100;
        int val2 = 200;
        val2 = val1 + val2;

        System.out.println(val2);



//      1.  Arithmetic operators









    }
}
